    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String maTimKiem = JOptionPane.showInputDialog(null, "Nhập mã cần tìm kiếm");
        NhanVien timKiem = a.timKiemTheoMa(maTimKiem);
        int chon = JOptionPane.showConfirmDialog(null, "Bạn muốn tìm kiếm thông tin "+maTimKiem);
        if(chon==0){
            if (timKiem!=null) {
                ArrayList<NhanVien> ketQua = new ArrayList<>();
                ketQua.add(timKiem);
                loadData(ketQua);
                setModal(timKiem);
                successfully("Tìm kiếm thành công");
            }
           else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy mã cần tìm.");
                error("Tìm kiếm thất bại");
            }     
       }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    public void setModal(NhanVien x){
        txtMa.setText(x.getMa());
        txtCauLacBo.setText(x.getCauLacBo());
        txtHoVaTen.setText(x.getHoTen());
        txtQuocTich.setText(x.getQuocTich());
        Date ngaySinh = x.getNgaySinh();
        txtNgaySinh.setText(ngaySinh.toString());
        txtLuongCoDinh.setText(""+x.getLuongCoDinh());
         int i = tblHienThi.getSelectedRow();        
        if(x.isGioiTinh()) {
                rbtNam.setSelected(true);
            }
            else {
                rbtNu.setSelected(true);
            }
        cbxNoiThiDau.setSelectedItem(x.getNoiCongTac());
        txtViTri.setText(x.getViTri());
        if(x instanceof CauThu){
                rbtCauThu.setSelected(true);
                txtChung1.setText(""+((CauThu) x).getSoBanThang());
                txtChung2.setText(""+((CauThu) x).getSoKienTao());
                txtChung3.setText(""+((CauThu) x).getSoCuuThua());
        }
        else if(x instanceof HuanLuyenVien){
                rbtHuanLuyenVien.setSelected(true);
                txtChung1.setText(""+((HuanLuyenVien)x).getSoNamKinhNghiem());
                txtChung2.setText(""+((HuanLuyenVien)x).getPhongCach());
                txtChung3.setText(""+((HuanLuyenVien)x).getTrinhDo());
            }       
    }   
