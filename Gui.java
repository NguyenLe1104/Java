    public void loadData(ArrayList<NhanVien> b){
        while(dtm.getRowCount()>0) dtm.removeRow(0);
        for(NhanVien x:b){
            if(x instanceof CauThu){
                dtm.addRow(new Object[]{x.getMa(),x.getHoTen(),x.getQuocTich(),x.getViTri(),x.getCauLacBo(),
                    x.getNoiCongTac(),x.getNgaySinh(),(x.isGioiTinh()== true?"Nam" :"Nu"),x.getLuongCoDinh(),((CauThu) x).getSoBanThang(),
                    ((CauThu) x).getSoKienTao(),((CauThu) x).getSoCuuThua(),"","","",x.getLuong()});
            }
            if(x instanceof HuanLuyenVien){
                dtm.addRow(new Object[]{x.getMa(),x.getHoTen(),x.getQuocTich(),x.getViTri(),x.getCauLacBo(),
                    x.getNoiCongTac(),x.getNgaySinh(),(x.isGioiTinh()== true?"Nam" :"Nu"),x.getLuongCoDinh(),"","","",((HuanLuyenVien) x).getSoNamKinhNghiem(),
                    ((HuanLuyenVien) x).getTrinhDo(),((HuanLuyenVien) x).getPhongCach(),x.getLuong()});
            }
        }
    }
