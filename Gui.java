    private void tblHienThiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHienThiMouseClicked
        // TODO add your handling code here:
        int i = tblHienThi.getSelectedRow();
        ArrayList<NhanVien> b = a.getA();
        NhanVien y = b.get(i);             
            if(y instanceof CauThu){
                rbtCauThu.setSelected(true);
                lblChung1.setText("Số bàn thắng");
                lblChung2.setText("Phong kiến tạo");
                lblChung3.setText("Số cứu thua");
                txtChung1.setText(""+((CauThu) y).getSoBanThang());
                txtChung2.setText(""+((CauThu) y).getSoKienTao());
                txtChung3.setText(""+((CauThu) y).getSoCuuThua());
        }
            else{
                rbtHuanLuyenVien.setSelected(true);
                lblChung1.setText("Số năm kinh nghiệm");
                lblChung2.setText("Phong cách");
                lblChung3.setText("Trình độ");
                txtChung1.setText(""+((HuanLuyenVien)y).getSoNamKinhNghiem());
                txtChung2.setText(""+((HuanLuyenVien)y).getPhongCach());
                txtChung3.setText(""+((HuanLuyenVien)y).getTrinhDo());
            }
            txtMa.setText(y.getMa());
            txtHoVaTen.setText(y.getHoTen());
            if(y.isGioiTinh()) {
                rbtNam.setSelected(true);
            }
            else {
                rbtNu.setSelected(true);
            }
            txtNgaySinh.setText(""+y.getNgaySinh());
            txtQuocTich.setText(y.getQuocTich());
            txtCauLacBo.setText(y.getCauLacBo());
            cbxNoiThiDau.setSelectedItem(y.getNoiCongTac());
            txtViTri.setText(y.getViTri());
            txtLuongCoDinh.setText(""+y.getLuongCoDinh());
        
     
    }//GEN-LAST:event_tblHienThiMouseClicked
    
