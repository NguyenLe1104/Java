    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty() || txtHoVaTen.getText().isEmpty() || txtQuocTich.getText().isEmpty() ||
                txtViTri.getText().isEmpty() ||  txtCauLacBo.getText().isEmpty() || txtNgaySinh.getText().isEmpty()|| 
                txtLuongCoDinh.getText().isEmpty() || txtChung1.getText().isEmpty() || txtChung1.getText().isEmpty() || txtChung3.getText().isEmpty() ) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin");
        return; 
    }
        String ma = txtMa.getText();
        String hoTen = txtHoVaTen.getText();
        String quocTich = txtQuocTich.getText();
        String vt = txtViTri.getText();
        String cauLacBo = txtCauLacBo.getText();
        String noiCongTac = (String) cbxNoiThiDau.getSelectedItem();
        Date ngaySinh = new Date(txtNgaySinh.getText());
        boolean gt = rbtNam.isSelected();
        double luongCoDinh = Double.parseDouble(txtLuongCoDinh.getText());
        NhanVien x;
        if(rbtCauThu.isSelected()){
            int soBanThang = Integer.parseInt(txtChung1.getText());
            int soKienTao = Integer.parseInt(txtChung2.getText());
            int soCuuThua = Integer.parseInt(txtChung3.getText());
            x = new CauThu(soBanThang, soKienTao, soCuuThua, ma, hoTen, quocTich, vt, cauLacBo, noiCongTac, ngaySinh, gt, luongCoDinh);
        }
        else{
            int soNamKinhNghiem = Integer.parseInt(txtChung1.getText());
            String trinhDo = txtChung2.getText();
            String phongCach = txtChung3.getText();
            x =  new HuanLuyenVien(soNamKinhNghiem, trinhDo, phongCach, ma, hoTen, quocTich, vt, cauLacBo, noiCongTac, ngaySinh, gt, luongCoDinh);
                }
        int chon = JOptionPane.showConfirmDialog(null, "Bạn muốn thêm thông tin");
        if(chon==0){
            if(a.themNhanVien(x)){
                loadData(a.getA());
                successfully("Đã thêm thành công");
            }
            else   { 
                JOptionPane.showMessageDialog(null,"Mã đã tồn tại trong danh sách");  
                error("Đã thêm thất bại");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

